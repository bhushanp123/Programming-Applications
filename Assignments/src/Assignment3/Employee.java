package Assignment3;

public class Employee {
	private int id;
	private String name;
	private int salary;
	
	public void setE_id(int id)
	{
		this.id = id;
	}
	public int getE_id()
	{
		return id;
	}
	public void setE_name(String name)
	{
		this.name = name;
	}
	public String getE_name()
	{
		return name;
	}
	public void setE_salary(int salary)
	{
		this.salary = salary;
	}
	public int getE_salary()
	{
		return salary;
	}

}

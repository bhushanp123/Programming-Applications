package class_object;

public class Book {
	String bookName;
	String author;
	int price;
	
	public void setBookName(String bookName)
	{
		this.bookName = bookName;
	}
	public String getBookName()
	{
		return bookName;
	}
	
	public void setAuthor(String author)
	{
		this.author = author;
	}
	public String getAuthor()
	{
		return author;
	}
	
	public void setPrice(int price)
	{
		this.price = price;
	}
	public int getPrice()
	{
		return price;
	}
}
